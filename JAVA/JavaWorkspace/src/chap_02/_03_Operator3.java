package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // true
        System.out.println(5 < 3); // false
        System.out.println(5 >= 3); // true
        System.out.println(5 <= 3); // false
        System.out.println(3 <= 3); // true

        System.out.println(5 == 5); // true
        System.out.println(5 == 3); // false
        System.out.println(5 != 5); // false
        System.out.println(5 != 3); // true

    }
}
